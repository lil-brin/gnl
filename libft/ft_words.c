#include "libft.h"

int	ft_words(char const *s, char c)
{
	int		i;
	int		words;

	i = 0;
	words = 0;
	if (!s || !c)
		return (0);
	while (s[i])
	{
		if (s[i] != c && (s[i + 1] == c || s[i + 1] == '\0'))
			words++;
		i++;
	}
	return (words);
}
